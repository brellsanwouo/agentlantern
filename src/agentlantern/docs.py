@dataclass(frozen=True)
class DocsResult:
    project_name: str
    framework: str
    output_dir: Path
    files: list[Path]


def generate_docs(project_root: Path, output_dir: Path | None = None) -> DocsResult:
    root = project_root.resolve()
    analyzer = detect_framework(root)
    project = analyzer.analyze(root)
    renderer = analyzer.get_renderer()

    docs_dir = output_dir.resolve() if output_dir else root / "docs"
    docs_dir.mkdir(parents=True, exist_ok=True)
    _remove_stale_agentlantern_docs(docs_dir)

    structure = renderer.get_doc_structure(project)
    markdown_files = dict(structure)
    files = {
        **markdown_files,
        "index.html": render_docsify_index(project),
        "_sidebar.md": renderer.render_sidebar(structure),
        "agentlantern-docs.html": render_share_html(project, markdown_files),
    }

    written_files: list[Path] = []
    for filename, content in files.items():
        path = docs_dir / filename
        path.write_text(content.rstrip() + "\n", encoding="utf-8")
        written_files.append(path)

    return DocsResult(
        project_name=project.name,
        framework=project.framework,
        output_dir=docs_dir,
        files=written_files,
    )


