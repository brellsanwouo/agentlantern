def _remove_stale_agentlantern_docs(docs_dir: Path) -> None:
    for filename in DOC_FILES | LEGACY_DOC_FILES | {"_coverpage.md"}:
        path = docs_dir / filename
        if path.exists():
            path.unlink()


def _doc_title(filename: str) -> str:
    return filename.removesuffix(".md").replace("-", " ").replace("_", " ").title()


def _slug(filename: str) -> str:
    return re.sub(r"[^a-z0-9]+", "-", filename.lower()).strip("-")


def _markdown_to_html(markdown: str) -> str:
    lines = markdown.splitlines()
    html_lines: list[str] = []
    table_lines: list[str] = []
    list_open = False
    code_open = False
    code_language = ""
    code_buffer: list[str] = []

    def close_list() -> None:
        nonlocal list_open
        if list_open:
            html_lines.append("</ul>")
            list_open = False

    def flush_table() -> None:
        nonlocal table_lines
        if table_lines:
            html_lines.append(_render_html_table(table_lines))
            table_lines = []

    for line in lines:
        if line.startswith("```"):
            if code_open:
                escaped = html.escape("\n".join(code_buffer))
                if code_language == "mermaid":
                    html_lines.append(f'<div class="mermaid">{escaped}</div>')
                else:
                    html_lines.append(f"<pre><code>{escaped}</code></pre>")
                code_open = False
                code_language = ""
                code_buffer = []
            else:
                close_list()
                flush_table()
                code_open = True
                code_language = line.removeprefix("```").strip()
            continue
        if code_open:
            code_buffer.append(line)
            continue
        if line.startswith("|") and line.endswith("|"):
            close_list()
            table_lines.append(line)
            continue
        flush_table()
        stripped = line.strip()
        if not stripped:
            close_list()
            continue
        if stripped.startswith("# "):
            close_list()
            html_lines.append(f"<h1>{_inline_markdown(stripped[2:])}</h1>")
        elif stripped.startswith("## "):
            close_list()
            html_lines.append(f"<h2>{_inline_markdown(stripped[3:])}</h2>")
        elif stripped.startswith("### "):
            close_list()
            html_lines.append(f"<h3>{_inline_markdown(stripped[4:])}</h3>")
        elif stripped.startswith("> "):
            close_list()
            html_lines.append(f"<blockquote><p>{_inline_markdown(stripped[2:])}</p></blockquote>")
        elif stripped.startswith("- "):
            if not list_open:
                html_lines.append("<ul>")
                list_open = True
            html_lines.append(f"<li>{_inline_markdown(stripped[2:])}</li>")
        else:
            close_list()
            html_lines.append(f"<p>{_inline_markdown(stripped)}</p>")

    close_list()
    flush_table()
    return "\n".join(html_lines)


def _render_html_table(lines: list[str]) -> str:
    rows = []
    for line in lines:
        cells = [cell.strip() for cell in line.strip("|").split("|")]
        if all(set(cell) <= {"-", " ", ":"} for cell in cells):
            continue
        rows.append(cells)
    if not rows:
        return ""
    header = "".join(f"<th>{_inline_markdown(cell)}</th>" for cell in rows[0])
    body = "\n".join(
        "<tr>" + "".join(f"<td>{_inline_markdown(cell)}</td>" for cell in row) + "</tr>"
        for row in rows[1:]
    )
    return f"<table><thead><tr>{header}</tr></thead><tbody>{body}</tbody></table>"


def _inline_markdown(value: str) -> str:
    escaped = html.escape(value)
    escaped = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", escaped)
    return re.sub(r"`([^`]+)`", r"<code>\1</code>", escaped)
